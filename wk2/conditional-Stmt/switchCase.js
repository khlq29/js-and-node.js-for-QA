/**
 * switch case
 */

let browser ="chrome"

switch(browser){
    case "chrome":
        console.log(">>>RUnning chrome browser");
        break
    case "firefox":
        console.log(">>>RUnning firefox browser");
        break
    case "edge":
        console.log(">>>RUnning edge browser");
        break
    default:
        console.log(`No valid  ${browser} browser is provided`);
}